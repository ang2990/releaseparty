            <div class="grid-item">
                <h1 class="text-huge">The vision</h1>
                <p class="text-large" style="margin-top: 20px;">This is an organizing tool for artists and listeners to coordinate a mass exit from major streaming platforms. We're building the infrastructure needed to bypass corporate gatekeepers and secure direct control over our music and income. This is about leverage, mobilization, and ending our reliance on systems that prioritize profit over people.</p>
            </div>
        </div>

        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">01.</span>
                <h2 class="text-large">User-centric payments</h2>
                <p style="margin-top: 20px;">The current pro-rata model pools subscription fees and distributes them based on total platform plays, effectively forcing you to fund artists you never listen to. We're organizing to force a move toward user-centric payments, where your money goes only to the artists you actually stream. This ends the bot economy and returns financial control to creators and fans.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">02.</span>
                <h2 class="text-large">Ethical divestment</h2>
                <p style="margin-top: 20px;">We demand that platforms stop using music revenue to fund military technology and border surveillance. This includes ending contracts with firms like Helsing and ICE. Our move is a direct action to stop our art from being used to normalize or finance violence and systemic harm.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">03.</span>
                <h2 class="text-large">Artist safety net</h2>
                <p style="margin-top: 20px;">Artists are currently denied the basic labor protections found in other fields. We're organizing to secure a fixed percentage of all revenue for a collective fund that provides healthcare and mental health support. This isn't a benefit; it's a necessary condition for a sustainable music industry.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">04.</span>
                <h2 class="text-large">AI consent</h2>
                <p style="margin-top: 20px;">We're taking a clear stand against the unauthorized use of our work to train AI models. We require explicit, opt-in consent and fair pay for any use of our likeness or creations. We will use our collective leverage to stop companies from appropriating our labor for their algorithms.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">05.</span>
                <h2 class="text-large">Ownership</h2>
                <p style="margin-top: 20px;">Artists must own their masters and their data. The current system often forces creators to lease their own work back from tech companies. We're building toward full sovereignty where artists control their own distribution and narratives, free from corporate ownership.</p>
            </div>
        </div>
