<div class="grid-container">
        <div class="grid-row">
            <div class="grid-item">
                <h1 class="text-huge">The Vision</h1>
                <p class="text-large" style="margin-top: 20px;">We're building a new way for artists to share their work and get paid. We want to move away from systems that exploit creators and prioritize algorithms over people. We're working toward a future where artists have control over their careers and their art.</p>
            </div>
        </div>

        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">01.</span>
                <h2 class="text-large">User-Centric Payments</h2>
                <p style="margin-top: 20px;">The current "pro-rata" model pools everyone's subscription money and distributes it based on total platform streams. This means your money often pays artists you've never listened to. We support a User-Centric Payment System (UCPS) where your subscription fee goes directly to the artists you actually stream. This model is fairer, harder to manipulate with bots, and ensures your support reaches the creators you care about.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">02.</span>
                <h2 class="text-large">Ethical Divestment</h2>
                <p style="margin-top: 20px;">We want platforms to stop investing in companies that profit from war or human rights abuses. This includes military technology firms like Helsing and advertising for agencies like ICE. Music should be about connection and community, not used to fund violence or surveillance.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">03.</span>
                <h2 class="text-large">Artist Safety Net</h2>
                <p style="margin-top: 20px;">Most artists lack the basic benefits found in other jobs. We're working to set aside a fixed percentage of platform revenue for a collective fund that helps cover artist healthcare and mental health services. This is about providing stability for creators so they can continue their work without constant financial risk.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">04.</span>
                <h2 class="text-large">AI Consent</h2>
                <p style="margin-top: 20px;">Generative AI should not use an artist's music or likeness without their explicit permission and fair pay. We believe creators should have control over whether their work is used to train AI models. We're advocating for clear rules that protect artists' rights and prevent companies from using their labor without consent.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <span class="text-accent text-large">05.</span>
                <h2 class="text-large">Ownership</h2>
                <p style="margin-top: 20px;">Artists should own their masters, their data, and the work they create. Many current platform agreements take control away from creators, essentially leasing their own art back to them. We want a future where artists have full control over how their music is distributed and sold, without being beholden to corporate gatekeepers.</p>
            </div>
        </div>
        <div class="grid-row">
            <div class="grid-item">
                <p class="text-large" style="margin-top: 20px; text-align: center;">This is about making the music industry work for creators and listeners again. Join us.</p>
            </div>
        </div>
